
using ApiLibreria.Data;
using ApiLibreria.Data.Repository;
using Microsoft.EntityFrameworkCore;
using Microsoft.Extensions.Options;

namespace ApiLibreria
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            builder.Services.AddDbContext<LibroDBContext>(options =>
            {
                options.UseSqlServer(builder.Configuration.GetConnectionString("ConectionDB"));
            });

            builder.Services.AddTransient<ILibroRepository, LibroRepository>();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
